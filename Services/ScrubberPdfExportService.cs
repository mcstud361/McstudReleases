        // ── Recovery Summary Bar ───────────────────────────────────────

        private void ComposeRecoverySummary(IContainer container, EstimateScoringResult result)
        {
            container.Background(Colors.Blue.Darken3).Padding(10).Row(row =>
            {
                row.RelativeItem().Column(col =>
                {
                    col.Item().Text("RECOVERY SUMMARY")
                        .FontSize(12).Bold().FontColor(Colors.White);

                    col.Item().PaddingTop(2).Text($"Score: {result.OverallScore}/100  ({result.Grade})")
                        .FontSize(10).FontColor(Colors.Blue.Lighten3);
                });

                row.AutoItem().AlignRight().AlignMiddle().Column(col =>
                {
                    var totalRecovery = result.PotentialCostRecovery;
                    if (totalRecovery > 0)
                    {
                        col.Item().Text($"Potential Recovery: ${totalRecovery:N2}")
                            .FontSize(12).Bold().FontColor(Colors.White);
                    }

                    var parts = new List<string>();
                    if (result.BodyLaborRecovery > 0) parts.Add($"Body: ${result.BodyLaborRecovery:F0}");
                    if (result.PaintLaborRecovery > 0) parts.Add($"Paint: ${result.PaintLaborRecovery:F0}");
                    if (result.MechLaborRecovery > 0) parts.Add($"Mech: ${result.MechLaborRecovery:F0}");
                    if (result.PartsRecovery > 0) parts.Add($"Parts: ${result.PartsRecovery:F0}");

                    if (parts.Count > 0)
                        col.Item().AlignRight().Text(string.Join("  |  ", parts))
                            .FontSize(9).FontColor(Colors.Blue.Lighten3);
                });
            });
        }

        // ── Footer ─────────────────────────────────────────────────────

        private void ComposeFooter(IContainer container)
        {
            container.AlignCenter().Text(text =>
            {
                text.Span("Page ");
                text.CurrentPageNumber();
                text.Span(" of ");
                text.TotalPages();
                text.Span(" | McStud Import Scrubber")
                    .FontSize(8).FontColor(Colors.Grey.Darken1);
            });
        }
